/**
 * @TASK P0-T2 - EngineResult 테이블 정의
 * @SPEC docs/planning/04-database-design.md#engine-result-table
 *
 * engine_result table — 엔진 분석 결과 (SEO/AEO/GEO·점수·실측값)
 *
 * 개념: x-sag의 diagnosis_items 및 점수 저장 구조를 차용하되,
 * boina에서는 diagnosis_id를 직접 참조하여 진단과 1:N 관계.
 *
 * 점수는 내부 저장만 (화면엔 신호등으로 변환 - 07 § 4).
 * category: seo, aeo, geo 중 하나.
 */

import {
  boolean,
  index,
  integer,
  jsonb,
  pgTable,
  text,
  timestamp,
  uuid,
} from "drizzle-orm/pg-core";
import { actionTypeEnum, categoryEnum, difficultyEnum, priorityEnum } from "../enums.js";
import { diagnoses } from "./diagnosis.js";

interface Evidence {
  url?: string;
  found?: string;
  expected?: string;
  [key: string]: unknown;
}

export const engineResults = pgTable(
  "engine_results",
  {
    /** UUID v4 primary key */
    id: uuid("id").primaryKey().defaultRandom(),

    /** Diagnosis reference */
    diagnosisId: uuid("diagnosis_id")
      .notNull()
      .references(() => diagnoses.id, { onDelete: "cascade" }),

    /** Storage channel: naver, google, ai_citation. UI maps ai_citation → ai; SNS is not a v1 channel. */
    channel: text("channel").notNull(),

    /** Category: seo, aeo, geo, a11y, backlink, perf */
    category: categoryEnum("category").notNull(),

    /** Action type: self_fix, snippet_action, vendor_action, si_action */
    actionType: actionTypeEnum("action_type").notNull(),

    /** Priority: high, medium, low */
    priority: priorityEnum("priority").notNull(),

    /** Difficulty: easy, medium, hard */
    difficulty: difficultyEnum("difficulty").notNull(),

    /**
     * Item code (stable identifier for deduplication)
     * e.g., "SEO_TITLE_MISSING"
     */
    code: text("code").notNull(),

    /**
     * Human-readable title in business-owner language
     * e.g., "제목에 지역명 포함 안 됨"
     */
    title: text("title").notNull(),

    /** Detailed description */
    description: text("description").notNull(),

    /**
     * Detection evidence (URL, found value, expected value, etc.)
     * Stored as JSONB for flexibility
     */
    evidence: jsonb("evidence").$type<Evidence>(),

    /**
     * Impact score (0-100, internal only)
     * Comment: "Internal scoring weight — DB에만 저장, UI 미노출"
     */
    impactScore: integer("impact_score"),

    /** Expected effect / benefit description */
    expectedEffect: text("expected_effect"),

    /** Whether generated by AI (vs. rule-based) */
    isAiGenerated: boolean("is_ai_generated").notNull().default(false),

    /** Related snippet type (for snippet_action items) */
    relatedSnippetType: text("related_snippet_type"),

    /** AI-assisted recommendation text */
    recommendationText: text("recommendation_text"),

    /** Page-specific scope (optional) */
    pageUrl: text("page_url"),

    /** Rule-set version that produced this item */
    ruleVersion: text("rule_version").notNull().default("1.0.0"),

    /** Created timestamp */
    createdAt: timestamp("created_at", { withTimezone: true }).notNull().defaultNow(),
  },
  (t) => [
    /** Index for diagnosis-based result queries */
    index("engine_results_diagnosis_id_idx").on(t.diagnosisId),
    /** Index for category-based filtering */
    index("engine_results_category_idx").on(t.category),
    /** Index for priority-based sorting */
    index("engine_results_priority_idx").on(t.priority),
    /** Composite index for diagnosis + category + priority */
    index("engine_results_diagnosis_category_priority_idx").on(
      t.diagnosisId,
      t.category,
      t.priority,
    ),
  ],
);

export type EngineResult = typeof engineResults.$inferSelect;
export type NewEngineResult = typeof engineResults.$inferInsert;
